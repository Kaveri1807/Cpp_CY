#include <iostream>
using namespace std;

int main() {
    int a = 8, b = 3;
    // addition operator
    cout << "a + b= " << (a + b) << endl;
    // substraction operator
    cout << "a - b " << (a - b) << endl;
    // Multiplication operator
    cout << "a * b " << (a*b) << endl;
    // Division  operator
    cout << "a / b " << (a / b) << endl;
    // Modulo  operator
    cout << "a % b= " << (a % b) << endl;
    return 0;
}
